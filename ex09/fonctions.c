int		ft_add(int val1, int val2)
{
	return (val1 + val2);
}

int		ft_sub(int val1, int val2)
{
	return (val1 - val2);
}

int		ft_mul(int val1, int val2)
{
	return (val1 * val2);
}

int		ft_div(int val1, int val2)
{
	return (val1 / val2);
}

int		ft_mod(int val1, int val2)
{
	return (val1 % val2);
}
