int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int cpt;

	i = 0;
	cpt = 0;
	while (tab[i] != 0)
	{
		if (f(tab[i]) == 1)
			cpt++;
		i++;
	}
	return (i);
}
